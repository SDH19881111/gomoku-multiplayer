// 3-3 규칙 유닛 테스트
// 실행: node --experimental-vm-modules backend/src/test_33.js

import { check33, createEmptyBoard, checkWin } from './utils.js';

let passed = 0;
let failed = 0;

function test(name, board, row, col, color, expected) {
  const result = check33(board, row, col, color);
  const ok = result === expected;
  if (ok) {
    passed++;
    console.log(`  ✅ ${name}`);
  } else {
    failed++;
    console.log(`  ❌ ${name} — expected ${expected}, got ${result}`);
  }
}

function boardFromStones(blackStones, whiteStones = []) {
  const b = createEmptyBoard();
  for (const [r, c] of blackStones) b[r][c] = 1;
  for (const [r, c] of whiteStones) b[r][c] = 2;
  return b;
}

console.log('\n=== 3-3 금수 테스트 ===\n');

// 테스트 1: 기본 십자 3-3
// 가로: (7,6) (7,7) (7,8) → 놓으면 _OOO_ (양끝 빈칸)
// 세로: (6,7) (7,7) (8,7) → 놓으면 _OOO_ (양끝 빈칸)
console.log('[1] 십자형 3-3 (가로+세로 활삼)');
{
  const b = boardFromStones([[7, 6], [7, 8], [6, 7], [8, 7]]);
  test('(7,7)에 놓으면 3-3 금수', b, 7, 7, 1, true);
}

// 테스트 2: 3-3이 아닌 경우 (단순 3)
console.log('\n[2] 한 방향만 활삼 (3-3 아님)');
{
  const b = boardFromStones([[7, 6], [7, 8]]);
  test('(7,7)에 놓으면 한 방향만 3 → 금수 아님', b, 7, 7, 1, false);
}

// 테스트 3: 막힌 3은 활삼이 아님
console.log('\n[3] 한쪽이 막힌 3 (활삼 아님)');
{
  // 가로: (7,5)에 상대 돌 → X_OOO_ 형태 (한쪽 막힘)
  // 세로: (5,7)에 상대 돌 → 마찬가지
  const b = boardFromStones(
    [[7, 6], [7, 8], [6, 7], [8, 7]], // 흑
    [[7, 5], [5, 7]]                   // 백 (막기)
  );
  test('양쪽 모두 한쪽 막힌 3 → 금수 아님', b, 7, 7, 1, false);
}

// 테스트 4: 대각선 + 가로 3-3
console.log('\n[4] 대각선 + 가로 3-3');
{
  // 가로: (7,6) (7,8) → 놓으면 _OOO_
  // 대각선↘: (6,6) (8,8) → 놓으면 _OOO_
  const b = boardFromStones([[7, 6], [7, 8], [6, 6], [8, 8]]);
  test('(7,7)에 놓으면 대각+가로 3-3 금수', b, 7, 7, 1, true);
}

// 테스트 5: 띈 3 + 연속 3 조합
console.log('\n[5] 띈삼(_O_OO_) + 연속삼(_OOO_)');
{
  // 가로: (7,5) (7,8) → 놓으면 (7,7)에서 _O_OO_ 형태? 
  // 아니, 좌표를 더 정확히 하자
  // 가로: (7,6) (7,8) → 놓으면 (7,7)에서 (7,5)_O(7,6)O(7,7)O(7,8)_ = _OOO_ (연속3)
  // 세로: (5,7) (6,7) → 놓으면 (7,7)에서 _(5)O(6)O(7)_ = _OO_ (그냥 2)
  // 다시 설계:
  // 가로: (7,6) (7,8) → (7,7)에 놓으면 _OOO_ ✓ 활삼
  // 세로: (5,7) (6,7) (8,7) → (7,7)에 놓으면 O(5)O(6)O(7)O(8) = 4연속... 아님 이건 활사
  // 다시:
  // 띈삼 예제: 가로에서 (7,5), (7,6) → (7,7)에 놓으면 OO_O? 아니..
  // 띈삼: (7,5) (7,7)에 놓으면 (7,5)O_(7,6빈)O(7,7) → 가로로 O_O 
  // 좀 더 명확하게:
  // 가로 방향 활삼: (7,5) (7,6) + (7,7)놓음 → _OOO_ 연속3 활삼 (7,4 빈, 7,8 빈)
  // 세로 방향 띈삼: (5,7) (8,7) + (7,7)놓음 → (5,7)O (6,7빈) (7,7)O (8,7)O → _O_OO_ 패턴
  //   (4,7)빈, (5,7)O, (6,7)빈, (7,7)O, (8,7)O, (9,7)빈 → _O_OO_ 활삼 ✓
  const b = boardFromStones([[7, 5], [7, 6], [5, 7], [8, 7]]);
  test('연속삼 + 띈삼 조합 → 3-3 금수', b, 7, 7, 1, true);
}

// 테스트 6: 빈 보드 중앙에 놓기 (금수 아님)
console.log('\n[6] 빈 보드에 첫 수 (금수 아님)');
{
  const b = createEmptyBoard();
  test('빈 보드 중앙 → 금수 아님', b, 7, 7, 1, false);
}

// 테스트 7: 4개 이미 있는 경우 (이것은 3-3이 아님, 4목임)
console.log('\n[7] 이미 4연속이 있는 경우');
{
  // 가로: (7,5)(7,6)(7,8)(7,9) + (7,7) → 5연속 → 이건 승리이지 3-3이 아님
  // 세로: (6,7)(8,7) → (7,7)놓으면 _OOO_ 활삼 1개
  // 3-3이 아니어야 함 (한 방향은 4이상이므로)
  const b = boardFromStones([[7, 5], [7, 6], [7, 8], [7, 9], [6, 7], [8, 7]]);
  // 가로 방향은 5연속이 되므로 활삼이 아니라 활사 이상
  // 세로 방향만 활삼 → 총 1개 → 금수 아님
  test('가로 5연속 + 세로 활삼 → 3-3 아님 (한쪽은 4이상)', b, 7, 7, 1, false);
}

// 테스트 8: 백돌에는 3-3 적용 안됨 (check33 자체는 색 무관하지만 server.js에서 흑만 체크)
// utils의 check33은 전달된 color에 대해 검사하므로, 백돌로도 테스트
console.log('\n[8] 백돌 3-3 테스트 (check33 함수 자체는 색 무관)');
{
  const b = boardFromStones([], [[7, 6], [7, 8], [6, 7], [8, 7]]);
  test('백돌 십자 3-3 패턴 → check33은 true', b, 7, 7, 2, true);
}

// 테스트 9: 경계에서의 3-3
console.log('\n[9] 보드 경계 근처 3-3');
{
  // (0,1)(0,2) → (0,3)놓으면 _ 없음 (벽) → 활삼 불가 (왼쪽이 벽)
  // 가로: (0,1)(0,3) → (0,2)에 놓으면 벽O(0,0빈)O(0,1)O(0,2)O(0,3)... 
  // 수정: (0,2)(0,3) → (0,4)에 놓으면 _(0,1빈)O(0,2)O(0,3)O(0,4)_(0,5빈) 활삼 ✓
  // 세로: (2,4)(3,4) → (4,4)에 놓으면 _(1,4빈)O(2)O(3)O(4)_(5,4빈) 활삼 ✓
  const b = boardFromStones([[0, 2], [0, 3], [2, 4], [3, 4]]);
  test('경계 근처 3-3 → 금수', b, 0, 4, 1, false); // (0,4)에 놓으면 가로 활삼 검사
  // 가로: (0,2)(0,3)(0,4) → _(0,1)OOO_(0,5) 활삼 ✓
  // 세로: (2,4)(3,4) + (0,4) → 0,4와 2,4 사이에 1칸 빈칸 → 연속 아님
  // 실제로: (0,4)돌, (1,4)빈, (2,4)돌, (3,4)돌 → O_OO 패턴 → _O_OO_ 될 수 있나?
  // 위: (-1,4)=벽, (0,4)=O, (1,4)=빈, (2,4)=O, (3,4)=O, (4,4)=빈
  // → 벽O_OO_ → 벽 때문에 활삼 아님 → 한 방향만 활삼 → 금수 아님 ✓
}

// 테스트 10: 띈삼 + 띈삼 조합 3-3
console.log('\n[10] 띈삼 + 띈삼 조합 3-3');
{
  // 가로 띈삼: (7,5)(7,8) + (7,7) → (7,5)O(7,6빈)(7,7)O(7,8)O → _O_OO_ 
  // (7,4빈)(7,5)O(7,6빈)(7,7)O(7,8)O(7,9빈) → _O_OO_ 활삼 ✓
  // 세로 띈삼: (5,7)(8,7) + (7,7) → (5,7)O(6,7빈)(7,7)O(8,7)O
  // (4,7빈)(5,7)O(6,7빈)(7,7)O(8,7)O(9,7빈) → _O_OO_ 활삼 ✓
  const b = boardFromStones([[7, 5], [7, 8], [5, 7], [8, 7]]);
  test('가로 띈삼 + 세로 띈삼 → 3-3 금수', b, 7, 7, 1, true);
}

console.log(`\n=== 결과: ${passed} passed, ${failed} failed ===\n`);
if (failed > 0) {
  process.exit(1);
}
